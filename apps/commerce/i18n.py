"""Commerce-side i18n infrastructure — Phase 3b (Session 45).

Mirrors apps/catalog/template_i18n.py in shape so a session that
already knows the preview-side pattern can read this file without
re-learning. Deliberately separate from the catalog dict because
commerce chrome talks about *buying* (cart, checkout, VAT,
tracking, returns) whereas the preview chrome talks about browsing
the live template (marketplace bar, language switcher in preview,
blog read-more). Keeping them split keeps each dict focused and
avoids bleed between the two surfaces.

Shape
-----
- SUPPORTED_LOCALES / DEFAULT_LOCALE / RTL_LOCALES re-exported from
  catalog so the two surfaces can never disagree on the set.
- COMMERCE_CHROME: locale → key → string. One flat dict per locale
  so a reviewer can scan a language end-to-end.
- resolve_locale(request) / get_chrome(locale) / html_dir(locale) /
  is_rtl(locale) / locale_switcher_entries(current) helpers mirror
  the preview-side helpers for parity.

Adding a key
------------
Add it to the IT block first (authoritative). Every other locale
block must carry the same key. Missing keys fall back to IT.
"""
