# ── Helpers ────────────────────────────────────────────────────────

def resolve_locale(request: HttpRequest) -> str:
    """Read `?lang=xx` from the request, validated.

    Falls back to DEFAULT_LOCALE on missing/unknown.
    """
    if request is None:
        return DEFAULT_LOCALE
    raw = (request.GET.get("lang") or "").strip().lower()
    if raw and raw in SUPPORTED_LOCALES:
        return raw
    return DEFAULT_LOCALE


def is_rtl(locale: str) -> bool:
    return locale in RTL_LOCALES


def html_dir(locale: str) -> str:
    return "rtl" if is_rtl(locale) else "ltr"


def get_chrome(locale: str) -> dict[str, str]:
    """Locale chrome dict, IT-backfilled for any missing key."""
    base = dict(COMMERCE_CHROME[DEFAULT_LOCALE])
    if locale != DEFAULT_LOCALE and locale in COMMERCE_CHROME:
        base.update(COMMERCE_CHROME[locale])
    return base


def locale_switcher_entries(current_locale: str) -> list[dict]:
    """All 5 locales as switcher rows. Commerce surface ships full coverage."""
    return [
        {
            "code":       code,
            "label":      LOCALE_LABELS[code],
            "badge":      LOCALE_BADGES[code],
            "is_current": code == current_locale,
        }
        for code in SUPPORTED_LOCALES
    ]


def localize(translations: dict, locale: str, field: str, fallback: str = "") -> str:
    """Pick a localized field out of a JSON translations dict.

    Shape: translations = {locale: {field: value}}. Missing locale or
    missing field falls back to `fallback` (typically the IT-native
    DB column value).
    """
    if not isinstance(translations, dict):
        return fallback
    block = translations.get(locale) or translations.get(DEFAULT_LOCALE) or {}
    value = block.get(field)
    if value in (None, ""):
        return fallback
    return value


def preserve_lang_qs(locale: str) -> str:
    """Return a ready-to-append query fragment (e.g. `?lang=en` or `""`).

    Used by templates that manually build hrefs to hand-propagate the
    current lang through internal links.
    """
    if locale == DEFAULT_LOCALE:
        return ""
    return f"?lang={locale}"
